    {
        $sk = '9bdf6d58fbb4c491a544cd5bcfe5ad919';
        $params = [
           'x_version' => '4.0',
            'x_country' => 'BR',
            'x_cpf' => '00003456789',
            'x_name' => 'Elijah Evans',
            'x_email' => 'testing2@astropaycard.com',
            'cc_number' => '4556993263529121',
            'cc_exp_month' => '06',
            'cc_exp_year' => '2019',
            'cc_cvv' => '554'
        ];
        /**
         * @var Pay $astropay;
